#include<iostream>

using namespace std;

int main()
{
    int a=4, b=5;
    cout<<"Operators in C++: \n";
    cout<<"TYPES:\n";
    cout<<"a+b : "<<a+b<<"\n";
    cout<<"a-b : "<<a-b<<"\n";
    cout<<"a*b : "<<a*b<<"\n";
    cout<<"a/b : "<<a/b<<"\n";
    cout<<"a%b : "<<a%b<<"\n";
    cout<<"a++b : "<<a++<<"\n";
    cout<<"a--b : "<<a--<<"\n";
    cout<<"++a : "<<++a<<"\n";
    cout<<"--a : "<<--a<<"\n";
    return 0;
}